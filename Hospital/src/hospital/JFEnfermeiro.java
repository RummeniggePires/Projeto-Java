    private void SalvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvaActionPerformed
        try {
            String nome = txtNome.getText();
            String cpf = txtCPF.getText();
            double salario = txtSalario.getText();
            String cre = txtCRE.getText();
            
            Enfermeiro enf = Enfermeiro (nome, cpf, salario, cre);
            
            DefaultTableModel model = (DefaultTableModel) tblEnfermeiro.getModel();
            model.addRow(new Object[]{enf.getNome(), enf.getCpf(), enf.salario, enf.cre});
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Informe corretamente: ");
        }
    }//GEN-LAST:event_SalvaActionPerformed

