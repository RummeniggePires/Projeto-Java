    public void preenchaTabela() {
        String[] colunas = new String[]{"Cód.", "Aluno", "Livro", 
            "Dt. Emp.", "Dt. Dev."};

        ArrayList dados = new ArrayList();

        Emprestimo emprestimo = new Emprestimo();
        ResultSet rs = emprestimo.listeEmprestimos();

        try {
            rs.first();
            do {
                dados.add(new Object[]{
                    rs.getInt("emprestimo_id"),
                    rs.getString("nome"),
                    rs.getString("titulo"),
                    rs.getString("data_emprestimo"),
                    rs.getString("data_devolucao")
                });
            } while (rs.next());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao preencer "
                    + "a tabela");
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        tblEmprestimo.setModel(modelo);
    }

