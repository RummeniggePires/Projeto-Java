    public void populeCbbAluno() throws SQLException {
        // cria uma instancia da classe aluno
        Aluno aluno = new Aluno();
        ResultSet rs = aluno.listeAlunos();

        // enquanto existir um próximo registro
        while (rs.next()) {

            // cria um novo objeto da classe aluno
            aluno = new Aluno();
            // determina os atributos nome e aluno_id do objeto
            aluno.setNome(rs.getString("nome"));
            aluno.setAlunoId(rs.getInt("aluno_id"));

            // adiciona à lista de objetos de alunos o objeto atual
            listaAluno.add(aluno);

            // determina o valor do cbbAluno
            cbbAluno.addItem(aluno.getNome());
        }
    }

    // cria uma lista de objetos de Livro
