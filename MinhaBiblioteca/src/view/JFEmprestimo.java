    public void populeCbbLivro() throws SQLException {
        // cria uma instancia da classe livro
        Livro livro = new Livro();
        ResultSet rs = livro.listeLivros();

        // enquanto existir um próximo registro
        while (rs.next()) {

            // cria um novo objeto da classe livro
            livro = new Livro();
            // determina os atributos titulo e livro_id do objeto
            livro.setTitulo(rs.getString("titulo"));
            livro.setLivroId(rs.getInt("livro_id"));

            // adiciona à lista de objetos de livros o objeto atual
            listaLivro.add(livro);

            // determina o valor do cbbLivro
            cbbLivro.addItem(livro.getTitulo());
        }
    }

